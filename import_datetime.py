import datetime

# Create today's date and time
now = datetime.datetime.now()

# Get current year
year = now.year

# Get current month
month = now.month

# Get current day
day = now.day

# Get current hour
hour = now.hour

# Get current minute
minute = now.minute

# Get current second
second = now.second

print(f"Year: {year}")
print(f"Month: {month}")
print(f"Day: {day}")
print(f"Hour: {hour}")
print(f"Minute: {minute}")
print(f"Second: {second}")